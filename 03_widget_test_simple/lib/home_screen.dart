import 'package:flutter/material.dart';

class HomeScreen extends StatelessWidget {
  final String title;
  final String message;

  const HomeScreen({
    Key? key,
    required this.title,
    required this.message,
  }) : super(key: key);

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      appBar: AppBar(
        title: Text(title),
      ),
      body: Center(
        child: Text(message),
      ),
    );
  }
}
